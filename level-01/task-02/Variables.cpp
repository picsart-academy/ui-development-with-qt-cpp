#include <iostream>

int main()
{
    // Variable declarations
    bool flag = false; // Declare a boolean variable named flag and initialize it to false
    char symbol = 'A'; // Declare a character variable named symbol and initialize it to 'A'
    int age = 33; // Declare an integer variable named age and initialize it to 33
    float pi = 3.14; // Declare a float variable named pi and initialize it to 3.14
    double pi_precise = 3.14159; // Declare a double variable named pi_precise and initialize it to 3.14159

    // Output variable values to the console
    std::cout << "Flag is " << flag << std::endl; // Output the value of flag
    std::cout << "Symbol is " << symbol << std::endl; // Output the value of symbol
    std::cout << "Age is " << age << std::endl; // Output the value of age
    std::cout << "Pi is " << pi << std::endl; // Output the value of pi
    std::cout << "Precise Pi is " << pi_precise << std::endl; // Output the value of pi_precise

    return 0;
}
